#include "mprintf.h"

void	mp_putchar(t_ptf *s, char c)
{
	write(1, &c, 1);
	s->ret++;
}

void	mp_putstr(t_ptf *s, char *str)
{
	int i;

	i = 0;
	while (str[i])
		mp_putchar(s, str[i++]);
}

void	mp_putint(t_ptf *s, int i)
{
	if (i == 0)
		mp_putchar(s, '0');
	if ((i / 10))
		mp_putint(s, i / 10);
	mp_putchar(s, i % 10 - '0');
}